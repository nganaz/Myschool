        Tense indicates the time of an action or event. The three basic tenses are Present, Past, and Future. Each of these has four aspects: simple, continuous (or progressive), perfect, and perfect continuous.

        1.  Present Tense:
            -   Simple Present: For habits, general truths, and regular actions. (e.g., He reads books.)
            -   Present Continuous: For actions happening now. (e.g., He is reading a book.)
            -   Present Perfect: For past actions with results in the present. (e.g., He has read the book.)
            -   Present Perfect Continuous: For actions that started in the past and continue to the present. (e.g., He has been reading for two hours.)

        2.  Past Tense:
            -   Simple Past: For completed actions in the past. (e.g., He read the book yesterday.)
            -   Past Continuous: For actions that were happening at a specific time in the past. (e.g., He was reading when I arrived.)
            -   Past Perfect: For an action that happened before another past action. (e.g., He had already read the book when I saw him.)
            -   Past Perfect Continuous: For a continuous action that was completed before another past action. (e.g., He had been reading for an hour before he left.)

        3.  Future Tense:
            -   Simple Future: For actions that will happen in the future. (e.g., He will read the book tomorrow.)
            -   Future Continuous: For actions that will be in progress at a time in the future. (e.g., He will be reading at 8 PM tonight.)
            -   Future Perfect: For actions that will be completed by a certain time in the future. (e.g., He will have read the book by next week.)
            -   Future Perfect Continuous: For a continuous action that will be completed at a point in the future. (e.g., By 10 PM, he will have been reading for three hours.)
