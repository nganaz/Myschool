        Example: The clever student reads a thick book.
        -   Complete Subject: The clever student
        -   Complete Predicate: reads a thick book

        Understanding Clauses:
        A clause is a group of words with a subject and a verb.
        -   Independent Clause: Expresses a complete thought and can stand alone as a sentence. (e.g., The dog barked.)
        -   Dependent (or Subordinate) Clause: Cannot stand alone as a complete sentence. It depends on an independent clause to form a complete thought. (e.g., ...because it was hungry.)

        Types of Sentences by Structure:
        1.  Simple Sentence: Contains one independent clause.
            -   Example: The cat slept.
        2.  Compound Sentence: Contains two or more independent clauses, joined by a conjunction.
            -   Example: The cat slept, and the dog barked.
        3.  Complex Sentence: Contains one independent clause and at least one subordinate clause.
            -   Example: The cat slept because it was tired.
        4.  Compound-Complex Sentence: Contains two or more independent clauses and at least one subordinate clause.
            -   Example: Because it was tired, the cat slept, and the dog barked.

