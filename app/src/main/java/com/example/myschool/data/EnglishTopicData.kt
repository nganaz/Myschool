        1.  Indefinite Articles (a, an): Used when referring to a general or non-specific noun.
            -   'a' is used before a word starting with a consonant sound. (e.g., a book, a university, a European)
            -   'an' is used before a word starting with a vowel sound. (e.g., an apple, an hour, an honest man)

        2.  Definite Article (the): Used for a specific noun that is known to the reader or speaker.
            -   Examples:
                -   The book on the table is mine. (A specific, identified book)
                -   The President of Malawi will visit our school. (A unique office)
                -   The moon is bright tonight. (A unique entity)

        3.  Zero Article (No Article): No article is used in some cases.
            -   With plural nouns in a general sense: Cats are good pets.
            -   With non-count nouns (mass nouns): I drink milk every day.
            -   With names of most countries, cities, and proper nouns: She lives in Blantyre. Thoko is a student.
            -   With abstract nouns in a general sense: Knowledge is power.
