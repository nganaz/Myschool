    Topic("parts_of_speech", "Parts of Speech",
        """
        In English, words are grouped into categories called Parts of Speech based on their function in a sentence. There are eight main parts of speech:

        1.  **Noun:** A name of a person, place, thing, or idea.
            *   Examples: *teacher, Zomba, book, knowledge*

        2.  **Pronoun:** A word used in place of a noun.
            *   Examples: *he, she, it, we, they, you*

        3.  **Verb:** Shows an action or a state of being.
            *   Examples: *run, read, is, am, were*

        4.  **Adjective:** Describes or modifies a noun or pronoun.
            *   Examples: *tall, beautiful, red, intelligent*

        5.  **Adverb:** Describes or modifies a verb, an adjective, or another adverb. It often tells how, when, where, or to what extent.
            *   Examples: *slowly, very, yesterday, here*

        6.  **Preposition:** Shows the relationship between a noun (or pronoun) and another word in the sentence.
            *   Examples: *in, on, at, under, behind*

        7.  **Conjunction:** Joins words, phrases, or clauses.
            *   Examples: *and, but, or, because*

        8.  **Interjection:** A word or phrase used to express strong emotion.
            *   Examples: *Oh!, Wow!, Alas!*
        """.trimIndent()),
    Topic("sentences", "Sentences",
        """
        A sentence is a group of words that expresses a complete thought. A complete sentence must have at least a subject and a predicate (verb).

        **Components of a Sentence:**
        *   **Subject:** Who or what the sentence is about.
        *   **Predicate:** What the subject does or is.

        Example: *The student reads a book.*
        *   Subject: *The student*
        *   Predicate: *reads a book*

        **Types of Sentences by Purpose:**

        1.  **Declarative:** Makes a statement. It ends with a full stop (.).
            *   Example: *Lake Malawi is beautiful.*

        2.  **Interrogative:** Asks a question. It ends with a question mark (?).
            *   Example: *Have you done your homework?*

        3.  **Imperative:** Gives a command, makes a request, or gives instructions. It usually ends with a full stop.
            *   Example: *Please pass the salt.*

        4.  **Exclamatory:** Expresses strong emotion or excitement. It ends with an exclamation mark (!).
            *   Example: *What a wonderful goal!*
        """.trimIndent()),
    Topic("tenses", "Tenses",
        """
        Tense indicates the time of an action or event. The three basic tenses are Present, Past, and Future.

        1.  **Simple Present Tense:**
            Used for habits, general truths, and regular actions.
            *   Examples:
                *   *The sun rises in the east.* (General truth)
                *   *She goes to the market every Saturday.* (Habit)

        2.  **Simple Past Tense:**
            Used for actions that were completed at a specific time in the past. Regular verbs often end in '-ed'.
            *   Examples:
                *   *He visited his uncle last week.*
                *   *We ate nsima for lunch.*

        3.  **Simple Future Tense:**
            Used for actions that will happen in the future. It is usually formed with 'will' or 'shall' + the base form of the verb.
            *   Examples:
                *   *I will finish the work tomorrow.*
                *   *They will play football after school.*
        """.trimIndent()),
    Topic("punctuation", "Punctuation",
        """
        Punctuation marks are symbols used in writing to separate sentences and their elements and to clarify meaning.

        1.  **Full Stop (.)**
            *   Used at the end of a declarative or imperative sentence.
            *   Example: *The school is closed. Be quiet.*

        2.  **Comma (,)**
            *   Separates items in a list. (e.g., *I need a pen, a book, and a ruler.*)
            *   Separates clauses in a compound sentence. (e.g., *He is poor, but he is honest.*)

        3.  **Question Mark (?)**
            *   Used at the end of a direct question.
            *   Example: *What is your name?*

        4.  **Exclamation Mark (!)**
            *   Used at the end of a sentence to show strong feeling.
            *   Example: *We won the match!*

        5.  **Apostrophe (')**
            *   Shows possession. (e.g., *This is Thoko's pen.*)
            *   Used in contractions to show where letters are omitted. (e.g., *don't* for 'do not', *it's* for 'it is'.)
        """.trimIndent()),
    Topic("articles", "Articles",
        """
        Articles are words that come before a noun. They specify whether the noun is general or specific.

        1.  **Indefinite Articles (a, an)**
            Used when talking about a general or non-specific noun.
            *   **'a'** is used before a word that starts with a consonant sound.
                *   Example: *a boy, a university, a European*
            *   **'an'** is used before a word that starts with a vowel sound.
                *   Example: *an apple, an hour, an honest man*

        2.  **Definite Article (the)**
            Used when talking about a specific noun that is known to both the speaker and the listener.
            *   Examples:
                *   *The book on the desk is mine.* (A specific book)
                *   *The President of Malawi will give a speech.* (There is only one President)

        3.  **Zero Article**
            Sometimes, no article is needed, especially with plural nouns used in a general sense or with non-count nouns.
            *   Example: *I like drinking water.* (General, non-count)
            *   *Students should work hard.* (Plural, general)
        """.trimIndent())
