        Punctuation marks are symbols used in writing to separate sentences and their elements and to clarify meaning.

        1.  Full Stop (.): Used at the end of a declarative or imperative sentence.

        2.  Comma (,): Separates items in a list, clauses, and introductory elements. It can also be used to separate adjectives.

        3.  Question Mark (?): Used at the end of a direct question.

        4.  Exclamation Mark (!): Used to show strong emotion or emphasis.

        5.  Apostrophe ('): Shows possession (Thoko's pen) or indicates a contraction (don't).

        6.  Quotation Marks ('' '' or " "): Used to indicate direct speech or to quote text. (e.g., She said, ''I am tired.'')

