        1.  Declarative: Makes a statement. It ends with a full stop (.).
            -   Example: Lake Malawi is the third-largest lake in Africa.

        2.  Interrogative: Asks a question. It ends with a question mark (?).
            -   Example: Have you ever visited Likoma Island?

        3.  Imperative: Gives a command, makes a request, or gives instructions. The subject is often an implied 'you'.
            -   Example: Please pass the salt. / Go and do your homework.

        4.  Exclamatory: Expresses strong emotion or excitement. It ends with an exclamation mark (!).
            -   Example: What a beautiful sunset over the lake!
