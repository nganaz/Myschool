            -   Examples: slowly, very, yesterday, here, too, always.
            -   It answers questions like: How? When? Where? How often? To what extent?

        6.  Preposition: Shows the relationship between a noun (or pronoun) and another word in the sentence.
            -   Examples: in, on, at, under, behind, with, for, from.
            -   It often indicates location, direction, or time.

        7.  Conjunction: Joins words, phrases, or clauses.
            -   Examples: and, but, or, because, so, although, while.
            -   Types: Coordinating (for, and, nor, but, or, yet, so), Subordinating (because, if, since), Correlative (either/or, neither/nor).

        8.  Interjection: A word or phrase used to express strong emotion. It is often followed by an exclamation mark.
            -   Examples: Oh!, Wow!, Alas!, Ouch!, Hey!
        """.trimIndent()),
